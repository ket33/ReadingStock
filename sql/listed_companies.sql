-- 1) 테이블
create table if not exists public.listed_companies (
  stock_code  text primary key,
  name        text not null,
  market      text not null,            -- 'KOSPI' | 'KOSDAQ'
  sector      text,
  updated_at  timestamptz not null default now()
);
create index if not exists idx_listed_name on public.listed_companies (name);

-- 2) RLS — 공개 명부이므로 누구나 읽기 가능, 쓰기는 service_role만
alter table public.listed_companies enable row level security;
drop policy if exists "listed_companies_read_all" on public.listed_companies;
create policy "listed_companies_read_all"
  on public.listed_companies for select
  using (true);

